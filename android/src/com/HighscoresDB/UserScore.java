public class UserScore {
    private String ID, user, score;

    public String getID() {
        return ID;
    }

    public String getUser() {
        return user;
    }

    public String getScore() {
        return score;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
